def add(a,b):
    answer  =   a   +   b
    return(answer)

def sustract(a,b):
    return  a   -   b

def multiply(a,b):
    return  a   *   b

def divide(a,b):
    answer  =   a   /   b
    return(answer)

def exp(a,b):
    return  a   **  b

print(exp(10,2))
print(add(7,90))
print(sustract(90,95))
print(multiply(8,5))
print(divide(9,3))

